kusa = pygame.image.load('kusa.png')
ground = pygame.image.load('ground.png')
buruki_migi = pygame.transform.scale(pygame.image.load('buru-ki-2.png'), (32, 32))
buruki_hidari = pygame.transform.scale(pygame.image.load('buru-ki-反対.png'), (32, 32))
buruki = buruki_migi
buruki_rect = buruki.get_rect()
buruki_rect.topleft = (0, 0)
clock = pygame.time.Clock()
def can_move_rect(rect):
    corners = [
        (rect.left, rect.top),
        (rect.right - 1, rect.top),
        (rect.left, rect.bottom - 1),
        (rect.right - 1, rect.bottom - 1)
    ]
    for x, y in corners:
        tile_x = x // tile_size
        tile_y = y // tile_size
        if not (0 <= tile_x < len(map_data[0]) and 0 <= tile_y < len(map_data)):
            return False
        if map_data[tile_y][tile_x] != 0:
            return False
    return True
running = True
while running:
    clock.tick(60)
    for e in pygame.event.get():
        if e.type == pygame.QUIT:
            running = False
    keys = pygame.key.get_pressed()
    new_x = buruki_rect.x
    new_y = buruki_rect.y
    if keys[pygame.K_LEFT]:
        new_x -= 10
        buruki = buruki_hidari
    if keys[pygame.K_RIGHT]:
        new_x += 10
        buruki = buruki_migi
    if keys[pygame.K_UP]:
        new_y -= 10
    if keys[pygame.K_DOWN]:
        new_y += 10
    temp_rect = buruki_rect.copy()
    temp_rect.x = new_x
    if can_move_rect(temp_rect):
        buruki_rect.x = new_x
    temp_rect = buruki_rect.copy()
    temp_rect.y = new_y
    if can_move_rect(temp_rect):
        buruki_rect.y = new_y
    screen.fill((0, 0, 0))
    for y in range(len(map_data)):
        for x in range(len(map_data[0])):
            tile = map_data[y][x]
            pos = (x * tile_size, y * tile_size)
            if tile == 0:
                screen.blit(kusa, pos)
            elif tile == 1:
                screen.blit(ground, pos)
    screen.blit(buruki, buruki_rect)
    pygame.display.flip()
pygame.quit()

